func (m Model) View() string {
	width := m.width
	if width < 56 {
		width = 56
	}
	composer := renderComposer(m.input.View(), width)
	footer := renderFooter(lipgloss.Width(composer))
	return renderBanner() + "\n" + composer + "\n" + footer
}

